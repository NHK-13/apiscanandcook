<?php

namespace App\Entity;
use ApiPlatform\Core\Annotation\ApiResource;
use Doctrine\Common\Collections\ArrayCollection;
use Doctrine\Common\Collections\Collection;
use Doctrine\ORM\Mapping as ORM;

/**
 * @ApiResource()
 * @ORM\Entity(repositoryClass="App\Repository\TypeRecetteRepository")
 */
class TypeRecette
{
    /**
     * @ORM\Id()
     * @ORM\GeneratedValue()
     * @ORM\Column(type="integer")
     */
    private $id;

    /**
     * @ORM\Column(type="string", length=150)
     */
    private $nomTypeRecette;

    /**
     * @ORM\OneToMany(targetEntity="App\Entity\Recette", mappedBy="typeRecette")
     */
    private $recette;

    public function __construct()
    {
        $this->recette = new ArrayCollection();
    }

    public function getId(): ?int
    {
        return $this->id;
    }

    public function getNomTypeRecette(): ?string
    {
        return $this->nomTypeRecette;
    }

    public function setNomTypeRecette(string $nomTypeRecette): self
    {
        $this->nomTypeRecette = $nomTypeRecette;

        return $this;
    }

    /**
     * @return Collection|Recette[]
     */
    public function getRecette(): Collection
    {
        return $this->recette;
    }

    public function addRecette(Recette $recette): self
    {
        if (!$this->recette->contains($recette)) {
            $this->recette[] = $recette;
            $recette->setTypeRecette($this);
        }

        return $this;
    }

    public function removeRecette(Recette $recette): self
    {
        if ($this->recette->contains($recette)) {
            $this->recette->removeElement($recette);
            // set the owning side to null (unless already changed)
            if ($recette->getTypeRecette() === $this) {
                $recette->setTypeRecette(null);
            }
        }

        return $this;
    }


    public function __toString()
    {
        return $this->nomTypeRecette;
    }
}
